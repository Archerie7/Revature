package com.revature.stringapi;

public class CharIndexDemo {
	public static void main(String[] args) {
		String str = "We work together as one team";
		
		char ch1=str.charAt(3);
		char ch2=str.charAt(9);
		
		System.out.println("Character at 3 index is: "+ch1);
		System.out.println("Character at 9 index is: "+ch2);
		
	}

}
